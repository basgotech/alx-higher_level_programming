-- Retrieves the 'score' and 'name' columns from the 'second_table' table
-- and orders the result set in descending order based on the 'score' column.
-- This SELECT statement fetches the 'score' and 'name' columns from the
-- 'second_table' table. The ORDER BY clause is used to sort the results
-- in descending order based on the values in the 'score' column.

SELECT score, name FROM second_table ORDER BY score DESC;
